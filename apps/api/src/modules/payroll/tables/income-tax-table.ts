// 100万円超の高額域は計算式で算出
// (課税対象額 × 税率 - 控除額) / (1 - 税率) の階段式
interface FormulaRange {
  lowerBound: number;
  upperBound: number;
  rate: number;       // 税率
  deduction: number;  // 控除額
  perDependent: number; // 扶養1人あたりの控除
}

const FORMULA_RANGES: FormulaRange[] = [
  { lowerBound: 1000000, upperBound: 1500000, rate: 0.2042, deduction: 123200, perDependent: 31667 },
  { lowerBound: 1500000, upperBound: 3000000, rate: 0.2042, deduction: 123200, perDependent: 31667 },
  { lowerBound: 3000000, upperBound: Infinity, rate: 0.2042, deduction: 123200, perDependent: 31667 },
];

/**
 * 源泉徴収税額を計算（月額表甲欄）
 * @param taxableSalary 課税対象額（総支給 - 社会保険料控除）
 * @param dependents 扶養人数（0〜7）
 */
export function calcWithholdingTax(taxableSalary: number, dependents: number): number {
  if (taxableSalary <= 0) return 0;
  const deps = Math.min(dependents, 7);

  // テーブル範囲内をルックアップ
  const row = WITHHOLDING_TAX_TABLE.find(
    (r) => taxableSalary >= r.lowerBound && taxableSalary < r.upperBound,
  );

  if (row) {
    return row.taxByDependents[deps] ?? row.taxByDependents[0];
  }

  // テーブル外（100万円超）: 計算式
  const formula = FORMULA_RANGES.find(
    (r) => taxableSalary >= r.lowerBound && taxableSalary < r.upperBound,
  ) ?? FORMULA_RANGES[FORMULA_RANGES.length - 1];

  const base = Math.round(taxableSalary * formula.rate - formula.deduction);
  const depDeduction = formula.perDependent * deps;
  return Math.max(0, base - depDeduction);
}
